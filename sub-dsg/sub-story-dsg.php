        <p>
        ‘을사조약'은 1905년 일본이 한국의 외교권을 박탈하기 위해
        강제로 체결한 조약으로, 상호 간에 맺은 조약이 아닌 강제적으로
        맺은 조약이란 뜻의 ‘을사늑약’이라고도 한다. 특히 5인의
        친일파에 의해 맺어진 조약이라 '을사 5적’이라는 명칭 갖는다. 
        박제순, 이지용, 이근택, 이완용, 권중현을 지칭. 이들이
        조약을 찬성했기에 터무니 없는 조약이 체결. 을사조약이 덕수궁
        중명전에서 체결되며 나라를 잃게 되는 역사의 장소가 된다.
