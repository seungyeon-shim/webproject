              금천교는 창덕궁의 돈화문과 진선문(進善門) 사이를 지나가는 명당수(明堂水)위에 설치되어 있다. <br>
              창덕궁의 명당수, 즉 금천(禁川)은 북쪽에서 남쪽으로 흘러내려 돈화문 오른쪽까지 와서 궐 밖으로 빠져나가는데,
              이 어구(御溝)물가에는 화강석 6-7단을 가지런하게 쌓은 축대를 설치하였고,
              여기에 금천교를 설치하여 궐내로 들어갈 수 있게 하였다.
              금천교는 창덕궁이 창건되고 6년 뒤 태종 11년(1411)3월 진선문 밖 어구에 설치되었는데,
              그 후 숱한 화재와 전란에도 불구하고 창건 당시의 모습을 보존하고 있다.
              현존하는 궁궐 안 돌다리 가운데 가장 오래된 것이다.
              규모는 의장(儀杖)을 갖춘 국왕의 행차 때 노부(鹵簿)의 폭에 맞도록 설정되었다.
