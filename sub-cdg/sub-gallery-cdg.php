              흥선대원군에 의해 경복궁이 중건되기 전까지 조선의 법궁(法宮) 역할을 하였다.
              또한 조선의 궁궐 중 가장 오랜 기간 동안 임금들이 거처했던 궁궐이다.
              경복궁의 주요 건물들이 좌우대칭의 일직선상으로 왕의 권위를 상징한다면
              창덕궁은 응봉자락의 지형에 따라 건물을 배치하여 한국 궁궐건축의 비정형적 조형미를 대표하고 있다.
